interface TabbleCellImageProps extends Omit<ImageProps, 'src'> {
  src?: string | StaticImport
}

export function TabbleCellImage({
  src,
  alt,
  className,
  ...props
}: TabbleCellImageProps) {
  return src ? (
    <Image
      className={cn('size-10 rounded aspect-square object-cover', className)}
      src={src}
      alt={alt}
      width={100}
      height={100}
      quality={100}
      loading="lazy"
      {...props}
    />
  ) : (
    <div className="size-10 rounded bg-muted"></div>
  )
}

